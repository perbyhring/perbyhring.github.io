    <summary>Slugs // Ημίθεος γυμνοσάλιαγκας</summary>
    <p class="byline">POETRY BY BRIAN SWANN // SHADERS BY <a href="https://twitter.com/perbyhring">PER BYHRING</a></p>
    <section class="body-text">
      <mark>Who could have dreamed them up? At least snails<br>
      have shells, but all these have is — nothing.</mark><br>
      Small black antennae like fat pins wave<br>
      as if they could take in enough to get them through.<br>
      Turn them over, they’re the soles of new shoes,<br>
      pale and unmarked as babies. They flow,<br>
      the soil itself learning how to move and, moving,<br>
      almost staying still, their silver monorail<br>
      the only evidence of where they’d been.<br>
      <mark>And they die quiet, or at least (thankfully)<br>
      out of the human ear’s range, between two stones,<br>
      under heels, shriveling in salt or piss, at the tips<br>
      of sharp sticks. Fight back, I hear myself say,<br>
      do something. Don’t just take it.</mark> But they die<br>
      as they had lived, exuding slime, like<br>
      the smaller boys, who’d just<br>
      stand there, miserable in short pants,<br>
      school socks down to their ankles,<br>
      school tie unknotted and askew, and flowing<br>
      from noses slow cauls of snot that<br>
      from time to time they’d lick or sniff back up<br>
      part way, until it flowed again, coating<br>
      the upper lip, falling into the mouth, mixing<br>
      with tears before anything had been done,<br>
      the fear itself enough, so even if we wanted<br>
      we couldn’t let them off. Sometimes it was<br>
      the knee “where you daren’t show your mother,”<br>
      other times the kick in the shins, the stick over<br>
      the head, the punch in the mouth, while they<br>
      just stood there, or double up, gasping<br>
      for breath, and we did it again.<br>
    </section>
    <p>Source: <a href="https://www.poetryfoundation.org/poetrymagazine/poems/55666/slugs">Poetry (July/August 2012)</a></p>
